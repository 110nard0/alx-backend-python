#!/usr/bin/env python3
"""Module 3-to_str contains a type-annotated function that
takes a float as argument and returns its string representation
"""


def to_str(n: float) -> str:
    """Accepts a float parameter and returns its string representation

    Args:
        n (float): Float argument

    Returns:
        (str): string representation of n
    """
    return str(n)
