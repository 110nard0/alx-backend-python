"""Module 0-add contains a type-annotated function that takes
two floats as arguments and returns a float value
"""


def add(a: float, b: float) -> float:
    """Accepts two float parameters and returns the sum

    Args:
        a (float): First float argument
        b (float): Second float argument

    Returns:
        (float): sum of a and b
    """
    return a + b
